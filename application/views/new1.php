                            <h3 class="tm-gold-text tm-form-title">ป.ป.ท.ตรวจโกดังนครสวรรค์ พบมีการทำเอกสาร แต่ไม่มีข้าวส่งปลายทาง</h3>
                            <p class="tm-form-description">เมื่อช่วงเที่ยง วันที่ 1 มิ.ย. นายประยงค์ ปรียาจิตต์ เลขาธิการสำนักงานป้องกันและปราบปรามการทุจริตในภาครัฐ (ป.ป.ท.) พล.ต.ท.ไกรบุญ ทรวดทรง ประธานกรรมการ องค์การคลังสินค้า (อ.ค.ส.) กระทรวงพาณิชย์ พร้อมคณะ เดินทางลงพื้นที่ตรวจสอบกรณี "คลังสินค้า ศิริวรรณ หลังที่ 2" เลขที่ 84 หมู่ 1 ต.ลำพยนต์ อ.ตากฟ้า จ.นครสวรรค์ ซึ่งเป็นที่เก็บรักษาข้าวของ อ.ค.ส. ประมาณ 1 หมื่นตัน ส่งให้กับปลายทางบริษัท สิงห์โตทอง ไรซ์ คอร์ปอเรชั่น จำกัด ที่อยู่ใน จ.กำแพงเพชร</p> 
                            <p class="tm-form-description">นายประยงค์ กล่าวว่า การตรวจสอบครั้งนี้ เพื่อเป็นการแก้ปัญหาเดิมและป้องกันไม่ให้เกิดขึ้นอีก โดยประสาน อ.ค.ส.ที่ระบุว่าได้ฝากข้าวไว้ที่คลังดังกล่าว 1 แสนกระสอบ หรือประมาณ 1 หมื่นตัน ตามความจุของคลัง เบื้องต้นทราบว่า คลังดังกล่าวต้องส่งสินค้ากระจายไปหลายจังหวัด ซึ่งต้องตรวจสอบอีกว่า จำนวนข้าวที่เหลือกระจายไปอยู่ที่ใด โดยโครงการรับจำนำข้าว ถือเป็นโครงการสำคัญถูกตรวจสอบเป็นคดีความ ตั้งแต่ปี 2556</p> 
                            <p class="tm-form-description">"ทั้งนี้ หลังตรวจสอบพบว่า ไม่มีข้าวส่งไปปลายทาง แต่มีการทำเอกสารส่งและรับข้าว อ.ค.ส.จึงแจ้งความดำเนินคดีกับหัวหน้าคลังสินค้า ในส่วนของ ป.ป.ท. ได้สั่งการให้ตรวจสอบเส้นทางข้าวจำนวน 1 แสนกระสอบ ที่ถูกเก็บไว้กับโกดังศิริวรรณ 2 ซึ่งพบสูญหายไปแล้ว 6,205.30 ตัน และกำลังตรวจสอบว่า ข้าวส่วนที่เหลือ สูญหายไปด้วยหรือถูกระบายไปยังจุดใด ทั้งนี้ ป.ป.ท. ตั้งอนุไต่สวนเป็นคดีใหม่ทำให้ขณะนี้มีสำนวนคดีที่เกี่ยวข้องจาก 987 สำนวนเป็น 990 สำนวน และยังมีคดีรอรับเพิ่มจากตำรวจอีก 9 สำนวน" นายประยงค์ กล่าว</p> 
